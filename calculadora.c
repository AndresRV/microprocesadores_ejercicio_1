#include "calculadora.h"

int suma(int valorUno, int valorDos)
{
	return valorUno+valorDos;
}
int resta(int valorUno, int valorDos)
{
	return valorUno-valorDos;
}
int division(int valorUno, int valorDos)
{
	return valorUno/valorDos;
}
int multiplicacion(int valorUno, int valorDos)
{
	return valorUno*valorDos;
}